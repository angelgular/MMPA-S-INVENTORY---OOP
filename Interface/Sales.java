    private void btnsaleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnsaleMouseClicked
        makebill();
        lorddata();
        try {
            // Check if there is enough stock for the sale
            String checkStockQuery = "SELECT `quantity` FROM `productList` WHERE `item_id` = ?";
            pst = con.prepareStatement(checkStockQuery);
            pst.setString(1, iid); // Match the item ID
            rs = pst.executeQuery();

            if (rs.next()) {
                int availableStock = rs.getInt("quantity");
                int saleQuantity = Integer.parseInt(quantity); // Quantity user wants to sell

                if (availableStock <= 0) {
                    JOptionPane.showMessageDialog(rootPane, "Sale not allowed. No stock available for this item.");
                    return; // Exit the method
                }

                if (saleQuantity > availableStock) {
                    JOptionPane.showMessageDialog(rootPane, "Sale not allowed. Requested quantity exceeds available stock.");
                    return; // Exit the method
                }

                // Insert sale details into the 'sales' table
                String q = "INSERT INTO `sales`(`sale_id`, `item_id`, `unit_price`, `quantity`, `total_price`, `discount`, `due`, `cash`, `change`, `date`) VALUES (?,?,?,?,?,?,?,?,?,?)";
                pst = con.prepareStatement(q);
                pst.setString(1, sid);
                pst.setString(2, iid);
                pst.setString(3, unitprice);
                pst.setString(4, quantity);
                pst.setString(5, totalprice);
                pst.setString(6, discount);
                pst.setString(7, due);
                pst.setString(8, cash);
                pst.setString(9, change);
                pst.setString(10, date);
                pst.execute();

                // Update product quantity in the 'productList' table
                String updateQuery = "UPDATE `productList` SET `quantity` = `quantity` - ? WHERE `item_id` = ?";
                pst = con.prepareStatement(updateQuery);
                pst.setInt(1, saleQuantity); // Subtract sold quantity
                pst.setString(2, iid); // Match the item ID
                pst.executeUpdate();

                JOptionPane.showMessageDialog(rootPane, "Sale successful and inventory updated.");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Item not found in inventory.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnsaleMouseClicked

