    private void itemlord() {
        try {
            String sql = "SELECT `item_name`, `serial_no`, `sale_price`, `image` FROM `productlist` WHERE item_id='" + txtid.getText() + "'";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {

                //txtid.setText(rs.getString("item_id"));
                txtname.setText(rs.getString("item_name"));
                txtserialno.setText(rs.getString("serial_no"));
                txtunit.setText(rs.getString("sale_price"));

                byte[] imagedata = rs.getBytes("image");
                format = new ImageIcon(imagedata);
                Image mm = format.getImage();
                Image img2 = mm.getScaledInstance(lblimage.getWidth(), lblimage.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon image = new ImageIcon(img2);
                lblimage.setIcon(image);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }

