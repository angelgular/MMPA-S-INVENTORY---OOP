    public class Database {

        private Connection con;
        private PreparedStatement pst;
        private ResultSet rs;

        public Database() {
            try {
                // Set up connection to your database (replace with your actual connection logic)
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventorysystem?","root","Mayangyan14.");
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

        public String getPasswordByUsername(String username) {
            String password = null;
            try {
                String sql = "SELECT `password` FROM `register` WHERE username=?";
                pst = con.prepareStatement(sql);
                pst.setString(1, username);  // Using PreparedStatement to prevent SQL Injection
                rs = pst.executeQuery();
                if (rs.next()) {
                    password = rs.getString("password");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            return password;
        }
    }

    //Inheritance
    public abstract class LoginHandler {

        public abstract boolean handleLogin(String username, String password);
    }

    public class RegularLoginHandler extends LoginHandler {

        private Database db;

        public RegularLoginHandler(Database db) {
            this.db = db;
        }

        @Override
        public boolean handleLogin(String username, String password) {
            String dbPassword = db.getPasswordByUsername(username);
            return dbPassword != null && dbPassword.equals(password);
        }
    }

