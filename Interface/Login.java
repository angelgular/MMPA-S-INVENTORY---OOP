        String password = txtpassword.getText();

        // Create Database object
        Database db = new Database();

        // Use polymorphism to handle login logic
        LoginHandler loginHandler = new RegularLoginHandler(db);

        if (loginHandler.handleLogin(username, password)) {
            Home home = new Home();
            home.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Username or Password is incorrect.");
        }
